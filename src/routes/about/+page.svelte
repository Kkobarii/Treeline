<h1 class="page-title">About Treeline</h1>

<div class="treeline-card">
	<div class="prose dark:prose-invert max-w-none">
		<h2 class="mt-0!">What is Treeline?</h2>
		<p>
			Treeline is an interactive learning platform designed to help students understand and visualize data structures and sorting
			algorithms in real-time. It offers step-by-step animations and interactive simulations to make complex concepts easier to
			understand.
		</p>

		<h2>Features</h2>
		<ul>
			<li><strong>Interactive Visualizations:</strong> Watch as data structures are built and modified in real-time</li>
			<li><strong>Step-by-Step Animations:</strong> Understand each operation with detailed step-by-step walkthroughs</li>
			<li><strong>Multiple Data Structures:</strong> Learn Binary Search Trees, AVL Trees, Red-Black Trees, and more</li>
			<li><strong>Educational Content:</strong> Detailed descriptions and explanations for each data structure</li>
		</ul>

		<h2>Supported Data Structures</h2>
		<p>Currently, Treeline supports the following data structures:</p>
		<ul>
			<li>Binary Search Tree (BST)</li>
			<li>AVL Tree</li>
			<li>Red-Black Tree</li>
		</ul>
		<p>
			I'm constantly adding new data structures and features as long as I'm able (and I'm not playing video games!). Also, there
			should definitely be some sorting algorithms coming soon™.
		</p>

		<h2>Technologies</h2>
		<p>
			Treeline is built using modern web technologies including Svelte, TypeScript, Tailwind CSS, and vis.js network for
			visualizations. The entire project is open-source and available on GitHub, though it's private for now as I'm scared of
			judgement.
		</p>

		<h2>Why am I doing this?</h2>
		<p>
			That is a question I keep asking myself too. The truth is, this is my Master's thesis project for my degree in Computer Science.
			I wanted to create something that would not only help me solidify my understanding of data structures and algorithms, but also
			provide value to others who are learning these concepts. Plus, I really enjoy coding something where I can see the results
			visually and this kind of project allows me to do just that.
		</p>

		<h2>Wish me luck!</h2>
		<p>
			If you find Treeline helpful or have any suggestions for improvements, feel free to reach out or contribute to the project once
			it's public. And until then, wish me luck on my thesis!
		</p>
	</div>
</div>
